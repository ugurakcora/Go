package main

import "fmt"

func main() {
		fmt.Println("Hello, World!")
}

// package main nedir? : main paketi, bir uygulamanın çalıştırılabilir olduğunu belirtir.
// import "fmt" nedir? : fmt paketini içeri aktarır. fmt paketi, biçimlendirilmiş giriş ve çıkış sağlar.
// func main() nedir? : main() fonksiyonu, bir uygulamanın başlangıç noktasıdır. Bir uygulama çalıştırıldığında, main() fonksiyonu otomatik olarak çalıştırılır.
// fmt.Println() nedir? : fmt.Println() fonksiyonu, bir dizeyi konsola yazdırmak için kullanılır.
// go run main.go komutu ile çalıştırılır.
// go build main.go komutu ile çalıştırılabilir dosya oluşturulur.
// go test main.go komutu ile test dosyası çalıştırılır.